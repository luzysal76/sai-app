  /** 날짜 기반 고정 카드 (오늘의 타로) */
  function getTodayCard() {
    const now = new Date();
    const seed = now.getFullYear() * 10000 + (now.getMonth() + 1) * 100 + now.getDate();
    return CARDS[seed % 22];
  }

  /** 랜덤 카드 (이전 카드 제외) */
  function drawCard(excludeId) {
    const pool = (excludeId !== undefined) ? CARDS.filter(c => c.id !== excludeId) : CARDS;
    const idx = Math.floor(Math.random() * pool.length);
    return pool[idx];
  }

  /** 종합 리딩 텍스트 생성 */
